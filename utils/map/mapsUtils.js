    constructor(numberTileHeight, numberTileWidth, blockHeight, blockWidth) {
        this.numberTileHeight = numberTileHeight;
        this.numberTileWidth = numberTileWidth;
        this.blockHeight = blockHeight;
        this.blockWidth = blockWidth;
    }

    getMapByName(mapName) {
        return this.maps.find(map => map.name === mapName);
    }

    setMapPng(gameMap) {
        document.getElementById('map').src = gameMap.src;
    }

    buildMap(gameMap) {
        gameMap.blocks.reverse();

        const blocksRows = [];
        let collisionBlocks = [];

        /** On construit une matrice à partir du tableau pour représenter la carte */
        for (let i = 0; i<this.numberTileHeight; i++) {
            blocksRows.push(gameMap.blocks.slice(i*30, i*30 + 30))
        }

        /** On transforme la carte en collision blocks */
        for (let i = 0; i<this.numberTileHeight; i++) {
            const row = []
            
            for (let j = 0; j<this.numberTileWidth; j++) {
                if (blocksRows[i][j] > 0) {
                    const collisionBlock = new CollisionBlock(j*this.blockWidth, i*this.blockHeight, this.blockWidth, this.blockHeight);
                    row.push(collisionBlock);
                }
            }

            if (row.length) collisionBlocks.push(row);
        }

        return collisionBlocks;
    }
